package ch_01;

public class q8 {
    public static void main(String[] args) {
        double radius, perimeter, area, pi;
        radius = 5.5;
        pi = 3.1416;

        perimeter = 2 * radius * pi;
        area = radius * radius * pi;

        System.out.println("Perimeter = " + perimeter);
        System.out.println("Area = " + area);

    }
}
